function sum(numero1, numero2) {
    return numero1 + numero2
}

function subs(numero1, numero2) {
    return numero1 - numero2;

}

function mult(numero1, numero2) {
    return numero1 * numero2;
}

function div(numero1, numero2) {
    return numero1 / numero2;
}
module.exports = { sum, subs, mult, div };